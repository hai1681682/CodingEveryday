// 究极简单，不用单调栈也能写
class Solution {
public:
    int maxArea(vector<int>& height) {
        int m = 0;
        for(int i = 0, j = height.size() - 1; i < j; ){
            int h = height[i] < height[j] ? height[i++] : height[j--];
            m = max(m, (j - i + 1) * h);
        }
        return m;
    }
};
