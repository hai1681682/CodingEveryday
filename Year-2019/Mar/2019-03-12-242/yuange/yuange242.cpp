class Solution {
public:
    bool isAnagram(string s, string t) {
        int l[26] = {0}, r[26] = {0};
        for (auto i : s) l[i - 'a']++;
        for (auto i : t) r[i - 'a']++;
        for (int i = 0; i < 26; ++i) if (l[i] != r[i]) return false;
        return true;
    }
};