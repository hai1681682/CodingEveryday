class MinStack {
private:
    stack<int> stVal;
    stack<int> stMin;
public:
    /** initialize your data structure here. */
    MinStack() {
        
    }
    
    void push(int x) {
        stVal.push(x);
        if (!stMin.empty()) stMin.push(min(x, stMin.top()));
        else stMin.push(x);
    }
    
    void pop() {
        stVal.pop();
        stMin.pop();
    }
    
    int top() {
        return stVal.top();
    }
    
    int getMin() {
        return stMin.top();
    }
};

