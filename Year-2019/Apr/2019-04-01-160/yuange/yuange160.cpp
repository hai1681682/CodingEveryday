/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode *getIntersectionNode(ListNode *headA, ListNode *headB) {
        ListNode *a = headA, *b = headB;
        int la = 0, lb = 0;
        while (a) {
            a = a->next;
            ++la;
        }
        while (b) {
            b = b->next;
            ++lb;
        }
        a = la > lb ? headA : headB;
        b = la > lb ? headB : headA;
        int d = abs(la - lb);
        while (d-- && a) a = a->next;
        while (a && b) {
            if (a == b) break;
            a = a->next;
            b = b->next;
        }
        return a;
    }
};