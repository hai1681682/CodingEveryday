/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
// 迭代版翻转链表，8ms，100%
class Solution {
public:
    ListNode* reverseList(ListNode* head) {
        if (!head) return nullptr;
        ListNode* m = head->next;
        head->next = nullptr;
        while (m) {
            ListNode* r = m->next;
            m->next = head;
            head = m;
            m = r;
        }
        return head;
    }
};